package com.oow;

import org.openjdk.jmh.annotations.Benchmark;
import org.openjdk.jmh.annotations.BenchmarkMode;
import org.openjdk.jmh.annotations.Fork;
import org.openjdk.jmh.annotations.Mode;
import org.openjdk.jmh.infra.Blackhole;

public class MyBenchmark {

    private static final int ITERATIONS = Math.max(Integer.getInteger("iterations", 1), 1);
    private static String sentence = "In 2019 I would like to run ALL languages in one VM.";

    @Benchmark
    @BenchmarkMode(Mode.AverageTime)
    @Fork(value = 1, warmups = 0)
    public void testMethod(Blackhole blackhole) {
        String sentence = String.join(" ", MyBenchmark.sentence);
        for (int iter = 0; iter < ITERATIONS; iter++) {
            if (ITERATIONS != 1) System.out.println("-- iteration " + (iter + 1) + " --");
            long total = 0, start = System.currentTimeMillis(), last = start;
            for (int i = 1; i < 10_000_000; i++) {
                total += sentence.chars().filter(Character::isUpperCase).count();
                if (i % 1_000_000 == 0) {
                    last = System.currentTimeMillis();
                    blackhole.consume(last);
                }
            }
            blackhole.consume(total);
        }
    }
}
